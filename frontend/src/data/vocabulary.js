export const getFallbackWordOfTheDay = () => {
  const start = new Date(new Date().getFullYear(), 0, 0);
  const today = new Date();
  const dayOfYear = Math.floor((today - start) / 86400000);
  return FALLBACK_WORDS[dayOfYear % FALLBACK_WORDS.length];
};

export const getFallbackQuiz = (count = 5) => {
  const shuffled = [...FALLBACK_WORDS].sort(() => Math.random() - 0.5).slice(0, count);
  return shuffled.map((word) => {
    const distractors = FALLBACK_WORDS.filter((candidate) => candidate.id !== word.id)
      .sort(() => Math.random() - 0.5)
      .slice(0, 3)
      .map((candidate) => candidate.definition);
    return {
      word_id: word.id,
      word: word.word,
      correct_definition: word.definition,
      options: [word.definition, ...distractors].sort(() => Math.random() - 0.5),
      is_offline: true,
    };
  });
};
